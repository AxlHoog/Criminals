  <tr><td class="subTitle"><a name="power"><b>Power</b></a></td></tr>
  <tr><td class="mainTxt">
Om sterker te worden moet je wapens en junkies/klonen/agenten krijgen, voor elke junkies/klonen/agenten gaat je power vijf
punten omhoog. Dit gaat een stuk sneller met wapens. De hoeveelheid power die je erbij krijgt van een wapen is het gemiddelde
van de attack en defence power van dat wapen. Door een wapen met 150 attack en 50 defence gaat je power dus 100 omhoog.
  </td></tr>
