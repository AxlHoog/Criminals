  <tr><td class="subTitle"><a name="faq"><b>FAQ</b></a></td></tr>
  <tr><td class="mainTxt">
<i><b>Wanneer staat iemand onder bescherming, en wat doet het?</b></i><br>
De eerste 12 uur nadat iemand zijn account heeft geactiveerd, staat hij onder bescherming. Dat wil zeggen dat niemand hem
aan kan vallen, maar ook hijzelf kan niemand aanvallen, doneren of geld gedoneerd krijgen. Deze periode is er vooral zodat
mensen veel kliks kunnen regelen zonder dat hun geld telkens wordt gestolen. Op die manier hebben ze na de eerste 24 uur
al wat wapens kunnen kopen en hebben ze dus een betere kans om hogerop te komen.<br><br>
<i><b>Ik ben mijn wachtwoord vergeten, wat nu?</b></i><br>
Ga naar login en klik op <a href="login.php?x=lostpass">Wachtwoord vergeten?</a>. Vul je e-mail in waarmee je je hebt
aangemeld bij criminals. Er zal een link worden verstuurd naar het opgegeven e-mail adres. Wanneer iemand op deze link
drukt zal er een nieuw wachtwoord ingesteld worden, en deze word naar het e-mail adres gestuurd. Wij raden u aan dit wachtwoord
meteen te veranderen.<br><br>
<i><b>Waar kan ik m'n wachtwoord veranderen?</b></i><br>
Je kunt je wachtwoord veranderen op de pagina "Profiel". Onderaan die pagina kun je je nieuwe wachtwoord invullen.
