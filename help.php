  <tr><td class="subTitle"><a name="geld"><b>Geld</b></a></td></tr>
  <tr><td class="mainTxt">
Elke speler krijgt elk uur $100 contant. Verder zijn er nog verschillende manieren om geld te krijgen in het spel:<br><br>

<div style="margin-left: 10px;">
<i>Kliks</i>
<div style="margin-left: 10px;">
Als iemand op jouw link klikt krijg je geld.
De hoeveelheid geld die je krijgt ligt aan het aantal junkies/klonen/agenten dat je al hebt:
<table style="margin-left: 10px;">
  <tr><td width=100>00 - 25</td>	<td>$1000 contant, $1000 op de bank</td></tr>
  <tr><td width=100>25 - 50</td>	<td>$750 contant, $750 op de bank</td></tr>
  <tr><td width=100>meer dan 50</td>	<td>$500 contant, $500 op de bank</td></tr>
</table>
</div><br>

<i>Bank</i>
<div style="margin-left: 10px;">
Over het bedrag dat op je bank staat krijg je elke dag 5% rente.
</div><br>

<i>Aanvallen</i>
<div style="margin-left: 10px;">
Je kan andere spelers aanvallen en hun geld stelen. Als je iemand aanvalt die bijna even sterk of sterker is dan jij dan heb
is er de kans dat je verliest en dat er geld van jou word gestolen...
</div><br>

<i>Wapens verkopen</i>
<div style="margin-left: 10px;">
Als je echt geld nodig hebt zou je je wapens kunnen verkopen. De opbrengst van een wapen is de helft van wat het wapen
oorspronkelijk gekost heeft.
</div><br>
</div>
  </td></tr>
